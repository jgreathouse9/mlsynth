"""Cross-validation: mlsynth CMBSTS vs the R ``CausalMBSTS`` package.

CMBSTS ports the multivariate Bayesian structural time series causal model of
Menchetti and Bojinov (2022) from the authors' ``CausalMBSTS`` R package. This
case runs the package's own vignette example -- a bivariate weekly series
(``3 sin(2t) + e``, ``2 cos(2t) + e``) with a fictional ``+2`` intervention at
2020-02-27, fit with a trend + cycle (period 75) model -- and checks the mlsynth
port against the R package cell-by-cell.

The data ship as ``basedata/cmbsts_vignette.csv`` (generated by R's vignette DGP,
``set.seed(1)``, so the noise is identical to the reference run). The reference
numbers were produced by the installed ``CausalMBSTS`` package with the same
Inverse-Wishart prior the estimator uses by default,
``s0.r = s0.eps = 0.01 * diag(var(y_pre))``, at ``niter=2000, burn=400``:

    temporal-average effect   R CausalMBSTS    mlsynth CMBSTS
    series 1 (treated)        2.4469           2.457
    series 2 (group)          2.2756           2.287
    series 1 95% CI lower     0.352            0.323
    series 1 95% CI upper     4.567            4.642

The two agree to about 0.01 on the posterior-mean effect and to a few hundredths
on the credible bounds -- within Monte-Carlo error, since the R and Python Gibbs
samplers draw from independent RNG streams (seed-faithful matching across the two
is not possible). The injected ``+2`` effect sits inside both credible bands.

Provenance: Menchetti & Bojinov (2022), AOAS 16(1): 414-435; the ``CausalMBSTS``
R package (Bojinov & Menchetti 2020), vignette "Working example of causal
inference with CausalMBSTS package".
"""
from __future__ import annotations

import os
import warnings

import numpy as np
import pandas as pd

_DATA = os.path.join(os.path.dirname(__file__), "..", "..", "basedata", "cmbsts_vignette.csv")
_INT_WEEK = 269  # number of pre-intervention weeks (2020-02-27 in the vignette)


def _long_panel() -> pd.DataFrame:
    v = pd.read_csv(os.path.abspath(_DATA))
    rows = []
    for t in range(len(v)):
        rows.append({"item": "store", "week": t, "y": float(v.y1[t]),
                     "treated": int(t >= _INT_WEEK)})
        rows.append({"item": "comp", "week": t, "y": float(v.y2[t]), "treated": 0})
    return pd.DataFrame(rows)


def run() -> dict:
    from mlsynth import CMBSTS

    with warnings.catch_warnings():
        warnings.simplefilter("ignore")
        res = CMBSTS({
            "df": _long_panel(), "outcome": "y", "unitid": "item", "time": "week",
            "treat": "treated", "group_units": ["comp"],
            "components": ["trend", "cycle"], "cycle_period": 75,
            "prior_scale": 0.01, "prior_rho": 0.0,
            "niter": 2000, "burn": 400, "seed": 1, "display_graphs": False,
        }).fit()
    d = res.inference_detail
    return {
        "s1_att": float(d.att_mean[0]),
        "s2_att": float(d.att_mean[1]),
        "s1_ci_lower": float(d.att_lower[0]),
        "s1_ci_upper": float(d.att_upper[0]),
        # the injected +2 effect must lie inside the treated-series credible band
        "effect_two_covered": float(d.att_lower[0] <= 2.0 <= d.att_upper[0]),
    }


# Deterministic (fixed seed). mlsynth reproduces R CausalMBSTS to ~0.01 on the
# posterior-mean effects and to a few hundredths on the bounds; the tolerance
# absorbs the residual cross-RNG Monte-Carlo difference.
EXPECTED = {
    "s1_att": (2.4469, 0.25),     # R CausalMBSTS treated-series temporal-average
    "s2_att": (2.2756, 0.25),     # R CausalMBSTS group-series temporal-average
    "s1_ci_lower": (0.352, 0.30),
    "s1_ci_upper": (4.567, 0.30),
    "effect_two_covered": (1.0, 0.0),
}
