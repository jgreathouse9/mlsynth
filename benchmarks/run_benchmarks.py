#!/usr/bin/env python3
"""Driver: run registered benchmark cases and report pass/fail vs tolerance.

    python benchmarks/run_benchmarks.py --all
    python benchmarks/run_benchmarks.py --case fdid_table5
    python benchmarks/run_benchmarks.py --with-reference   # include R cross-checks
"""
from __future__ import annotations

import argparse
import sys
import time
from pathlib import Path

# allow "python benchmarks/run_benchmarks.py" from the repo root
sys.path.insert(0, str(Path(__file__).resolve().parents[1]))

import json   # noqa: E402

from benchmarks import registry                          # noqa: E402
from benchmarks.compare import compare, BenchmarkSkipped  # noqa: E402

_REF = Path(__file__).resolve().parent / "reference"


def run_one(name: str):
    """Run a single case. Returns a structured result dict (status in
    pass/fail/skip, per-metric rows, timing) -- and prints the usual summary."""
    mod = registry.load(name)
    t0 = time.time()
    try:
        got = mod.run()
    except BenchmarkSkipped as exc:
        print(f"\n[SKIP] {name}   ({exc})")
        return {"name": name, "status": "skip", "reason": str(exc),
                "seconds": round(time.time() - t0, 1), "metrics": []}
    expected = mod.EXPECTED
    ok, report = compare(got, expected)
    rows = []
    for key, (exp, tol) in expected.items():
        g = got.get(key)
        rows.append({"metric": key, "got": g, "expected": exp, "tol": tol,
                     "pass": (g is not None and abs(g - exp) <= tol)})
    print(f"\n[{'PASS' if ok else 'FAIL'}] {name}   ({time.time()-t0:.0f}s)")
    print(report)
    return {"name": name, "status": "pass" if ok else "fail",
            "seconds": round(time.time() - t0, 1), "metrics": rows,
            **_case_meta(name)}


def _case_meta(name: str) -> dict:
    """Description (from the case docstring) and, when present, the captured
    reference bundle's manifest + provenance."""
    mod = registry.load(name)
    doc = (mod.__doc__ or "").strip().splitlines()
    meta = {"description": doc[0].strip() if doc else ""}
    bundle = _REF / name
    if (bundle / "manifest.json").exists():
        manifest = json.loads((bundle / "manifest.json").read_text())
        meta.update(paper=manifest.get("paper"),
                    reference_impl=manifest.get("reference_impl"),
                    path_type=manifest.get("path_type"),
                    reference_bundle=f"benchmarks/reference/{name}/")
        if (bundle / "provenance.json").exists():
            prov = json.loads((bundle / "provenance.json").read_text())
            meta["reference_provenance"] = {
                k: prov.get(k) for k in ("generated_at", "git_sha", "r_version", "packages")}
    return meta


def write_report(results: list, out_dir: Path) -> None:
    """Write report.json (machine-readable) and REPORT.md (the dashboard)."""
    report_json = out_dir / "report.json"
    report_json.write_text(json.dumps(results, indent=2, default=str) + "\n")
    lines = ["# Benchmark validation report", "",
             "Generated by `python benchmarks/run_benchmarks.py --report`. Each row is a",
             "registered benchmark case; cases with a captured reference bundle link to",
             "the exact reference run and its provenance.", "",
             "| Case | Status | Type | Reference | Bundle |",
             "|------|--------|------|-----------|--------|"]
    icon = {"pass": "PASS", "fail": "FAIL", "skip": "skip"}
    for r in results:
        bundle = (f"[`{r['reference_bundle']}`]({r['reference_bundle']})"
                  if r.get("reference_bundle") else "—")
        lines.append(f"| `{r['name']}` | {icon.get(r['status'], r['status'])} "
                     f"| {r.get('path_type', '—')} | {r.get('reference_impl', '—')} | {bundle} |")
    n_pass = sum(r["status"] == "pass" for r in results)
    n_fail = sum(r["status"] == "fail" for r in results)
    n_skip = sum(r["status"] == "skip" for r in results)
    lines += ["", f"{n_pass} passed, {n_fail} failed, {n_skip} skipped.", ""]
    (out_dir / "REPORT.md").write_text("\n".join(lines) + "\n")
    print(f"\nwrote {report_json} and {out_dir / 'REPORT.md'}")


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--all", action="store_true")
    ap.add_argument("--case", default=None)
    ap.add_argument("--with-reference", action="store_true",
                    help="also run cases that require an R reference implementation")
    ap.add_argument("--report", action="store_true",
                    help="write benchmarks/REPORT.md and benchmarks/report.json")
    args = ap.parse_args()

    names = ([args.case] if args.case else list(registry.CASES))
    if not args.with_reference:
        names = [n for n in names if n not in registry.NEEDS_REFERENCE]

    results = [run_one(n) for n in names]
    n_pass = sum(r["status"] == "pass" for r in results)
    n_skip = sum(r["status"] == "skip" for r in results)
    n_fail = sum(r["status"] == "fail" for r in results)
    if args.report:
        write_report(results, Path(__file__).resolve().parent)
    tail = f" ({n_skip} skipped)" if n_skip else ""
    print(f"\n==== {n_pass}/{n_pass + n_fail} benchmarks passed{tail} ====")
    return 0 if n_fail == 0 else 1


if __name__ == "__main__":
    raise SystemExit(main())
